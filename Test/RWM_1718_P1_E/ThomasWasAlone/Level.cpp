Level::Level() 
{

}

void Level::init(float worldUnit)
{
	m_currentLevel = 1;
	m_pointerToLevel = (int*)level1Layout;
	m_worldUnit = worldUnit;
}

void Level::setupLevel(std::vector<GameObject*> &objects, Renderer &renderer) 
{
	int currentVectorItem = 0;

	if (!objects.empty()) 
	{
		objects.clear();
	}
	for (int i = 0; i < maxCol; i++)
	{
		for (int j = 0; j < maxRow; j++)
		{
			if (m_pointerToLevel[maxRow * i + j] == 1)
			{
				objects.push_back(new Platform(Point2D(j * m_worldUnit, i * m_worldUnit), m_worldUnit));
				objects.at(currentVectorItem)->type = GameObject::GameObjectType::PLATFORM;
				currentVectorItem++;
			}
			else if (m_pointerToLevel[maxRow * i + j] == 2)
			{
				objects.push_back(new Portal(Rect(j * m_worldUnit, i * m_worldUnit, m_worldUnit, m_worldUnit * 2), (Colour(122, 155, 255))));
				objects.at(currentVectorItem)->type = GameObject::GameObjectType::PORTAL;
				currentVectorItem++;
			}
			else if (m_pointerToLevel[maxRow * i + j] == 3)
			{
				spawnPoint = Point2D(j * m_worldUnit, i * m_worldUnit);
			}
			else if (m_pointerToLevel[maxRow * i + j] == 4)
			{
				objects.push_back(new Water(Point2D(j * m_worldUnit, i * m_worldUnit), m_worldUnit));
				objects.at(currentVectorItem)->type = GameObject::GameObjectType::WATER;
				currentVectorItem++;
			}
			else if (m_pointerToLevel[maxRow * i + j] == 5)
			{
				objects.push_back(new RespawnBox(Rect(j * m_worldUnit, i * m_worldUnit, m_worldUnit * 3, m_worldUnit * 10), renderer));
				objects.at(currentVectorItem)->type = GameObject::GameObjectType::RESPAWNBOX;
				currentVectorItem++;
			}
		}
	}
}

void Level::SwitchLevel()
{
	if (m_currentLevel == 1)
	{
		m_pointerToLevel = (int*)level1Layout;
	}
	else if (m_currentLevel == 2)
	{
		m_pointerToLevel = (int*)level2Layout;
	}
	else if (m_currentLevel == 3)
	{
		m_pointerToLevel = (int*)level3Layout;
	}
	else if (m_currentLevel == 4)
	{
		m_pointerToLevel = (int*)level4Layout;
	}
	else if (m_currentLevel == 5)
	{
		m_pointerToLevel = (int*)level5Layout;
	}
}

